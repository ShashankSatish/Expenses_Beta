    private int validateInput() {
        if(txtLoginId.getText().isEmpty()||txtUsername.getText().isEmpty()||txtPassword.getPassword().length==0||txtRetypePwd.getPassword().length==0||txtBudget.getText().isEmpty()) {
            return 0;
        }
        
        if(txtLoginId.getText().length()<6||txtPassword.getPassword().length<6) {
            return -1;
        }
        
        if(String.valueOf(txtPassword.getPassword()).equals(String.valueOf(txtRetypePwd.getPassword()))==false) {
            return -2;
        }
        
        if(txtBudget.getText().length()>5||txtBudget.getText().length()<3){
            return -3;
        }
        
        return 1;
    }

    private String getGender() {
        if(jrbMale.isSelected()){
            return jrbMale.getText();
        }
        if(jrbFemale.isSelected()){
            return jrbFemale.getText();
        }
        return null;
    }

    private int getAge() {
        int selectedIndex=jcAge.getSelectedIndex();
            if(selectedIndex==0) {
                return 0;
            }
            //System.out.println("Age"+jcAge.getSelectedItem());
            return (Integer.parseInt(jcAge.getSelectedItem().toString()));
    }
