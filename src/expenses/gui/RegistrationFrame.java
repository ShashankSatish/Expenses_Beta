    private void btnSaveRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveRegisterActionPerformed
        // TODO add your handling code here:
        int inputValidity=validateInput();
        
        if(inputValidity==0) {
                JOptionPane.showMessageDialog(null, "Please fill all the required fields","Fill All Fields!",JOptionPane.ERROR_MESSAGE);
            }
            else if(inputValidity==-2){
                JOptionPane.showMessageDialog(null, "Password must be identical in both fields","Re-Type Passowrd!",JOptionPane.ERROR_MESSAGE);
            }
            else if(inputValidity==-1) {
                JOptionPane.showMessageDialog(null, "Login ID/Password must have minimum 6 characters","ID/Password too short!",JOptionPane.ERROR_MESSAGE);
            }
            else if(inputValidity==-3) {
                JOptionPane.showMessageDialog(null, "Enter values within 100-100000","Invalid Range",JOptionPane.ERROR_MESSAGE);
            }
            else{
                uGender=getGender();
                if(uGender==null){
                    JOptionPane.showMessageDialog(null, "Please select a Gender","Select One",JOptionPane.ERROR_MESSAGE);
                    return;
                }
                
                cbAge=getAge();
                if(cbAge==0){
                    JOptionPane.showMessageDialog(null, "Please select your age","Age not Found!",JOptionPane.ERROR_MESSAGE);
                    return;
                }
                
                uid=txtLoginId.getText();
                pwd=String.valueOf(txtPassword.getPassword());
                uname=txtUsername.getText();
                budget=Integer.parseInt(txtBudget.getText());
                
                Users u=new Users();
                    u.setUserId(uid);
                    u.setPassword(pwd);
                    
                UserInfo uinfo=new UserInfo();
                    uinfo.setUserId(uid);
                    uinfo.setName(uname);
                    uinfo.setGender(uGender);
                    uinfo.setAge(cbAge);
                    
                try{
                    result1=UsersDAO.userIdExists(uid);
                    if(!result1){
                        //JOptionPane.showMessageDialog(null, "Welcome to our App","Hello"+uid,JOptionPane.INFORMATION_MESSAGE);
                
                        try{
                            result2=UserInfoDAO.userNameExists(uname);
                                if(!result2){
                                    boolean a,b;
                                    a=UsersDAO.addUsers(u);
                                    b=UserInfoDAO.addUserInfo(uinfo);
                                    
                                    if(a&&b) {
                                        CategoriesDAO.addToCat(uid, budget);
                                        JOptionPane.showMessageDialog(null, "Your info has been added suceesfully..","Welcome to our app "+uname,JOptionPane.INFORMATION_MESSAGE);
                                        UserLoginFrame frame=new UserLoginFrame();
                                            frame.setVisible(true);
                                            this.dispose();
                                    }
                                    else {
                                    JOptionPane.showMessageDialog(null, "Your account could not be created","Sorry! Please try again!",JOptionPane.ERROR_MESSAGE);
                                    }
                                }
                                else{
                                    JOptionPane.showMessageDialog(null, "Please try another name","User name already exists",JOptionPane.ERROR_MESSAGE);
                                }        
                        }
                        catch(SQLException ex) {
                            JOptionPane.showMessageDialog(null, "Could not update user_info table ","SQL Exception",JOptionPane.ERROR_MESSAGE);
                            ex.printStackTrace();
                        }
                    }
                    
                    else{
                        JOptionPane.showMessageDialog(null, "Please input another User ID","User id already exists",JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }    
                catch(SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Could not update users table ","SQL Exception",JOptionPane.ERROR_MESSAGE);
                    ex.printStackTrace();
                }
                
                
                
            }
               
    }//GEN-LAST:event_btnSaveRegisterActionPerformed

